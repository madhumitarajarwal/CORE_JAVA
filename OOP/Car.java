package com.OOP;

public class Car {
	String Model;
	String color;
	int speed=0;
	int gear=0;
	
	public String getcolor() {
		return color;
	}
	public void setcolor(String color) {
		this.color=color;
	}
	public String getModel() {
		return Model;
	}
	public void setModel(String Model) {
		this.Model=Model;
	}
	public int getspeed() {
		return speed;
	}
	public void setspeed(int speed) {
		this.speed=speed;
	}
	public int getgear() {
		return gear;
	}
	public int setgear(int gear) {
		return this.gear=gear;
	}
	
	

}
